package com.iotdbResultCSV.dom;

public class analyzeResultBean {
	
	private int id;
	private String projectID;
	private String operation;
	private String result_key;
	private String result_value;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getProjectID() {
		return projectID;
	}
	public void setProjectID(String projectID) {
		this.projectID = projectID;
	}
	public String getOperation() {
		return operation;
	}
	public void setOperation(String operation) {
		this.operation = operation;
	}
	public String getResult_key() {
		return result_key;
	}
	public void setResult_key(String result_key) {
		this.result_key = result_key;
	}
	public String getResult_value() {
		return result_value;
	}
	public void setResult_value(String result_value) {
		this.result_value = result_value;
	}

}
